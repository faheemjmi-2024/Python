# Fruit Ripeness Checker

fruit = "Banana"

color = "Yellow"

if fruit == "Banana":
    if color == "Green":
        print("Unripe")
    elif color == "Yellow":
        print("Ripe")
    elif color == "Brown":
        print("OverRipe")

if fruit == "Apple":
    if color == "Green":
        print("Unripe")
    elif color == "Red":
        print("Ripe")
    elif color == "Brown":
        print("OverRipe")

